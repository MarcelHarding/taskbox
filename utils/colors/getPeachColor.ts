const rgbaToHex = ({ r, g, b, a }: { r: number, g: number, b: number, a: number }): string => {
  const to255 = (v: number) => Math.round(v * 255);
  const hex = (v: number) => v.toString(16).padStart(2, "0");
  return `#${hex(to255(r))}${hex(to255(g))}${hex(to255(b))}${hex(Math.round(a * 255))}`;
};

export const getPeachColor = (colorName: string, darkMode: boolean): string => {
  const color = themeColors[colorName];
  if (!color) return rgbaToHex({ r: 1, g: 1, b: 1, a: 1 });

  return darkMode ? rgbaToHex(color.dark) : rgbaToHex(color.light);
};

